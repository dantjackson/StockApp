package com.stocker.rest;

public class Stocks {

	String StockId;
	String StockName;
	String StockExc;
	public String getStockId() {
		return StockId;
	}
	public void setStockId(String stockId) {
		StockId = stockId;
	}
	public String getStockName() {
		return StockName;
	}
	public void setStockName(String stockName) {
		StockName = stockName;
	}
	public String getStockExc() {
		return StockExc;
	}
	public void setStockExc(String stockExc) {
		StockExc = stockExc;
	}
	
	
	
}
